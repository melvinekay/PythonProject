#A function (a method) is a block of code expected to perform a task
#Built-In Functions
#max()
y = max(34, 78, 56, 10)
print(y)
#min()
x = min(45, 90, 65, 43)
print(x)
#pow()
z = pow(10, 2)
print(z)

#User-Defined Functions
def greeting():
    print("Hello There!!!")
greeting() #Calling a function

def doctor(name):
    print(name)
print(doctor("Mel"))
print(doctor("Jab"))
print(doctor("Brad"))
