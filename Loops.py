#While loop
#Increasing values
number = 20
while number <= 25:
    print(number)
    number += 1

number1 = 105
while number1 >= 100:
    print(number1)
    number1 -= 1

#For loop
for x in range(1,5):
    print(x)
for num in range(20,30,2):
    print(num)

#List
languages = ["Python", "Kotlin", "PHP"]
for lang in languages:
    print(lang)