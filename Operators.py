#Arithmetic Operators
num1 = 34
num2 = 66

print(num1 + num2) #Addition
print(num1 - num2) #Subtraction
print(num1 * num2) #Multiplication
print(num1 / num2) #Division
print(num1 // num2) #Flow Division
print(num1 % num2) #Modulus
print(num1 ** num2) #Power

#Comparison Operators
print(num1 < num2) #Lesser than
print(num1 > num2) #Greater than
print(num1 >= num2) #Lesser than or Equal to
print(num1 <= num2) #Greater than or Equal to
print(num1 != num2) #Not Equal to
print(num1 == num2) #Equal to

#Assignment Operators
w = 7
print(w)
w += 3
print(w)

b = 10
print(b)
b -= 2
print(b)

#Logical Operators - AND, OR, & NOT
print(56 < 100 and 74 > 86)
print(56 < 100 or 74 > 86)
print(not(56 < 100 and 74 > 86))
print(not(56 < 100 or 74 > 86))